public class DataPelanggan {
    int id;
    String nama, alamat, email;
    int telepon;

    public DataPelanggan(int id, String nama, String alamat, int telepon, String email) {
        this.id = id;
        this.nama = nama;
        this.alamat = alamat;
        this.telepon = telepon;
        this.email = email;
    }
}